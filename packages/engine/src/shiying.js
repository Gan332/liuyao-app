/**
 * 获取世应位置
 * @param {object} hexagram - 卦象对象（含 num 属性）
 * @returns {{shi: number, ying: number}}
 */
export function getShiYing(hexagram) {
  const shi = SHI_YING_MAP[hexagram.num];
  const ying = (shi + 3) % 6;
  return { shi, ying };
}

/**
 * 对六爻应用世应标记
 * @param {object} hexagram - 卦象对象
 * @param {Array} lines - 可选，用于标记对应爻的世应
 * @returns {Array<{shi: boolean, ying: boolean}>}
 */
export function applyShiYing(hexagram, lines) {
  const { shi, ying } = getShiYing(hexagram);
  const count = lines ? lines.length : 6;
  return Array.from({ length: count }, (_, i) => ({
    shi: i === shi,
    ying: i === ying,
  }));
}
